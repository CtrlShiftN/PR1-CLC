package W02.Activities;

public class BMICal {
    public static void main(String[] args) {
        double w = 61.5, h = 1.71;
        double bmi = w / (h*h);
        System.out.println("My weight: " + w + " (kg)");
        System.out.println("My height: " + h + " (m)");
        System.out.println("My BMI: " + bmi + " (kg/m2)");
        
    }        
}
